<?php
namespace channels;


class BaseCharge
{
    protected $params = [];

    /**
     * 设置sdk参数
     * @param mixed $args
     * @return $this
     */
    public function setArgs($args)
    {
        foreach ($args as $key => $value) {
            $this->params[$key] = $this->{$key} = $value;
        }
        return $this;
    }
}
